import { products } from './products';

function findProduct(id: string) {
  return products.find((p) => p.id === id)!;
}

function orderItem(productId: string, qty: number, overrides: Record<string, unknown> = {}) {
  const p = findProduct(productId);
  return {
    id: `oi-${productId}-${Math.random().toString(36).slice(2, 8)}`,
    order_id: '',
    product_id: productId,
    variant_id: null,
    product_name: p.name,
    product_image_url: p.images[0]?.thumbnail_url ?? null,
    unit_price: p.sale_price ?? p.base_price,
    quantity: qty,
    total_price: (p.sale_price ?? p.base_price) * qty,
    discount_amount: 0,
    special_instructions: null,
    substitution_product_id: null,
    status: 'confirmed',
    ...overrides,
  };
}

