export function computeRiderStats(riderList: MockRider[]): MockRiderStats {
  const byVehicle: Record<VehicleType, number> = {
    bicycle: 0,
    motorcycle: 0,
    car: 0,
    van: 0,
    walking: 0,
  };

  let totalRating = 0;
  let ratedCount = 0;

  for (const r of riderList) {
    byVehicle[r.vehicle_type] = (byVehicle[r.vehicle_type] || 0) + 1;
    if (r.rating_average > 0) {
      totalRating += r.rating_average;
      ratedCount++;
    }
  }

  return {
    total: riderList.length,
    active: riderList.filter((r) => r.status === 'active').length,
    pending: riderList.filter((r) => r.status === 'pending').length,
    suspended: riderList.filter((r) => r.status === 'suspended').length,
    inactive: riderList.filter((r) => r.status === 'inactive').length,
    online: riderList.filter((r) => r.is_online).length,
    offline: riderList.filter((r) => !r.is_online).length,
    by_vehicle_type: byVehicle,
    avg_rating: ratedCount > 0 ? Math.round((totalRating / ratedCount) * 100) / 100 : 0,
    total_deliveries_today: riderList.reduce((sum, r) => sum + r.today_deliveries, 0),
    total_earnings_today: riderList.reduce((sum, r) => sum + r.today_earnings, 0),
  };
}
